#!/bin/bash

items=(
    "\033[0;34m->\033[0m->->->->->->"
    "->\033[0;34m->\033[0m->->->->->"
    "->->\033[0;34m->\033[0m->->->->"
    "->->->\033[0;34m->\033[0m->->->"
    "->->->->\033[0;34m->\033[0m->->"
    "->->->->->\033[0;34m->\033[0m->"
    "->->->->->->\033[0;34m->\033[0m"
)
for i in "${items[@]}"; do
    printf "%b\r" "$i";
    sleep 0.5
done

